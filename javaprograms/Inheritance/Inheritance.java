import java.util.*;
public class Inheritance
{
    public static void main(String[] args)
    {
        int[] arr1={10,20,30,40};
        String[] arr2={"Rajendra","porte"};

        for(int x: arr1)
        {
            System.out.print(x+" " + " ");

        }
        for(String x2:arr2)
        {
            System.out.print(x2+" " + " ");
        }
        
    }
}